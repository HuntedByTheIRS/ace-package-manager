fn test_load_pkg_full_glibc() {
	if check_pkg_skipped(glibc_path) {
		return
	}
	pkg := load_pkg_full(glibc_path) or {
		assert false, 'load_pkg_full(glibc) failed: ${err}'
		return
	}

	// Metadata
	assert pkg.name == 'glibc'
	assert pkg.version == '2.43+r22+g8362e8ce10b2-4'

	// Scriptlet detection (.INSTALL present)
	assert pkg.scriptlet == true

	// File list should have entries
	assert pkg.files.files.len > 0

	// Should include known files and dirs
	mut found_etc := false
	for f in pkg.files.files {
		if f.name == 'etc/' {
			found_etc = true
			break
		}
	}
	assert found_etc, 'expected "etc/" in file list'
}

fn test_load_pkg_full_acl() {
	if check_pkg_skipped(acl_path) {
		return
	}
	pkg := load_pkg_full(acl_path) or {
		assert false, 'load_pkg_full(acl) failed: ${err}'
		return
	}

	// Metadata
	assert pkg.name == 'acl'

	// No .INSTALL in acl
	assert pkg.scriptlet == false

	// acl has ~102 total entries: .BUILDINFO, .MTREE, .PKGINFO + ~99 files/dirs
	assert pkg.files.files.len > 90

	// Verify specific files exist
	mut files_found := map[string]bool{}
	for f in pkg.files.files {
		files_found[f.name] = true
	}
	assert files_found['usr/bin/chacl'] or { false }
	assert files_found['usr/bin/getfacl'] or { false }
	assert files_found['usr/bin/setfacl'] or { false }
	assert files_found['usr/include/acl/libacl.h'] or { false }
	assert files_found['usr/lib/libacl.so.1.1.2302'] or { false }

	// Symlinks
	assert files_found['usr/lib/libacl.so'] or { false }
	assert files_found['usr/lib/libacl.so.1'] or { false }
}

