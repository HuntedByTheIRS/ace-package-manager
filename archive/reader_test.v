const glibc_path = '/var/cache/pacman/pkg/glibc-2.43+r22+g8362e8ce10b2-4-x86_64_v3.pkg.tar.zst'

const acl_path = '/var/cache/pacman/pkg/acl-2.3.2-2.1-x86_64_v3.pkg.tar.zst'

fn check_pkg_skipped(path string) bool {
	if !os.exists(path) {
		return true
	}
	// Also skip if we don't have permissions
	if !os.is_file(path) {
		return true
	}
	return false
}

fn test_load_pkg_metadata_glibc() {
	if check_pkg_skipped(glibc_path) {
		return
	}
	pkg := load_pkg_metadata(glibc_path) or {
		assert false, 'load_pkg_metadata(glibc) failed: ${err}'
		return
	}

	assert pkg.name == 'glibc'
	assert pkg.version == '2.43+r22+g8362e8ce10b2-4'
	assert pkg.desc == 'GNU C Library'
	assert pkg.url == 'https://www.gnu.org/software/libc'
	assert pkg.arch == 'x86_64_v3'
	assert pkg.packager == 'CachyOS <admin@cachyos.org>'
	assert pkg.build_date == 1777750710
	assert pkg.isize == 53144214
	assert pkg.base == 'glibc'

	// Licenses
	assert pkg.licenses.len == 2
	assert 'GPL-2.0-or-later' in pkg.licenses
	assert 'LGPL-2.1-or-later' in pkg.licenses

	// Dependencies
	assert pkg.depends.len >= 3
	mut found_dep := map[string]bool{}
	for dep in pkg.depends {
		found_dep[dep.name] = true
	}
	assert found_dep['linux-api-headers'] or { false }
	assert found_dep['tzdata'] or { false }
	assert found_dep['filesystem'] or { false }

	// Check versioned dependency
	for dep in pkg.depends {
		if dep.name == 'linux-api-headers' {
			assert dep.version == '4.10'
			assert dep.modifier == .ge
			break
		}
	}

	// Optional dependencies
	assert pkg.optdepends.len >= 2
	mut found_opt := map[string]bool{}
	for dep in pkg.optdepends {
		found_opt[dep.name] = true
	}
	assert found_opt['gd'] or { false }
	assert found_opt['perl'] or { false }

	// Origin should be file
	assert pkg.origin == PackageOrigin.file

	// Scriptlet should NOT be set by metadata-only load
	assert pkg.scriptlet == false
}

