fn test_parse_pkginfo_basic() {
	data := 'pkgname = testpkg\npkgver = 1.0-1\npkgdesc = Test description\nurl = https://example.com\narch = any\nbuilddate = 1234567890\npackager = Test User <test@example.com>\nsize = 987654\n'
	mut pkg := Package{}
	parse_pkginfo(data.bytes(), mut pkg)

	assert pkg.name == 'testpkg'
	assert pkg.name_hash == compute_name_hash('testpkg')
	assert pkg.version == '1.0-1'
	assert pkg.desc == 'Test description'
	assert pkg.url == 'https://example.com'
	assert pkg.arch == 'any'
	assert pkg.build_date == 1234567890
	assert pkg.packager == 'Test User <test@example.com>'
	assert pkg.isize == 987654
}

fn test_parse_pkginfo_multivalue_fields() {
	data := 'pkgname = multi-pkg\npkgver = 2.0\nlicense = MIT\nlicense = Apache-2.0\ndepend = libfoo>=1.0\ndepend = libbar\ndepend = libbaz<3.0\nprovides = virtual-foo\nconflict = old-foo\nreplaces = legacy-foo\nbackup = /etc/foo.conf\nbackup = /etc/foo/bar.conf\ngroup = foo-group\n'
	mut pkg := Package{}
	parse_pkginfo(data.bytes(), mut pkg)

	assert pkg.name == 'multi-pkg'

	// Licenses accumulate
	assert pkg.licenses.len == 2
	assert 'MIT' in pkg.licenses
	assert 'Apache-2.0' in pkg.licenses

	// Dependencies
	assert pkg.depends.len == 3
	mut found_libfoo := false
	mut found_libbar := false
	mut found_libbaz := false
	for dep in pkg.depends {
		if dep.name == 'libfoo' {
			found_libfoo = true
			assert dep.version == '1.0'
			assert dep.modifier == .ge
		}
		if dep.name == 'libbar' {
			found_libbar = true
			assert dep.version == ''
			assert dep.modifier == .any
		}
		if dep.name == 'libbaz' {
			found_libbaz = true
			assert dep.version == '3.0'
			assert dep.modifier == .lt
		}
	}
	assert found_libfoo
	assert found_libbar
	assert found_libbaz

	// Provides / conflicts / replaces
	assert pkg.provides.len == 1
	assert pkg.provides[0].name == 'virtual-foo'
	assert pkg.conflicts.len == 1
	assert pkg.conflicts[0].name == 'old-foo'
	assert pkg.replaces.len == 1
	assert pkg.replaces[0].name == 'legacy-foo'

	// Backup files
	assert pkg.backup.len == 2
	assert pkg.backup[0].name == '/etc/foo.conf'
	assert pkg.backup[1].name == '/etc/foo/bar.conf'

	// Groups
	assert pkg.groups.len == 1
	assert pkg.groups[0] == 'foo-group'
}

